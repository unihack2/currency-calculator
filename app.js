const baseurl = "https://v6.exchangerate-api.com/v6/ee75d0d114b1a272b2a950fc/latest/";
let select = document.querySelectorAll(".main select");
const from = document.querySelector(".from select");
const to = document.querySelector(".to select");
let msg = document.querySelector(".exrate p");
for (let sel of select) {
  for (code in countryList) {
    let newop = document.createElement("option");
    newop.innerText = code;
    newop.value = code;
    if (sel.name === "from" && code === "USD") {
      newop.selected = "selected";
    } else if (sel.name === "to" && code === "INR") {
      newop.selected = "selected";
    }
    sel.append(newop);
  }
  sel.addEventListener("change", (evt) => {
    changeflag(evt.target);
  })
}
const changeflag = (element) => {
  let cntrycode = countryList[element.value];
  let newsrc = 'https://flagsapi.com/' + cntrycode + '/flat/64.png';
  let img = element.parentElement.querySelector("img");
  img.src = newsrc;
};
let btn = document.getElementById("exbt");
btn.addEventListener("click", async () => {
  msg.addEventListener("change",()=>{
    alert("exchange rate not available");
  })
  let amt = document.querySelector("#ip");
  let amtval = amt.value;
  if (amtval === "" || amtval < 1) {
    amtval = 1;
    amt.value = "1";
  }
  let url = baseurl + from.value;
  let response = await fetch(url);
  let data = await response.json();
  for (cn in countryList) {
    if (to.value === cn) {
      let rate = data.conversion_rates[cn];
      let finalamt = amtval * rate;
      msg.innerText = amtval + ' ' + from.value + '  =  ' + finalamt + ' ' + to.value;
      if (finalamt !== NaN) {
        console.log();
      }else {
        alert("exchange rate unavalable");
      }
    }
  }

  amtval = 1;
  amt.value = "1";
})
